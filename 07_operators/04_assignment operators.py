# Assignment Operators:

# =, +=, -=, *=, /=, %=, **=, //=.

a=4
print(a)

# this operator add value in orginal value of a
a+=4
print(a)

# substract it from original  value
a=5 
a-=5
print(a)

# multiply it by origiinal value
a=3
a*=3
print(a)

# division //  floor value
a=43
a//=4
print(a)

# It takes the variable on the left, raises it to a power,
#  and stores the result back in the same variable.
a=2
a**=4
print(a)