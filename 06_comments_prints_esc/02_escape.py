# Escape Sequences
# Escape sequences are used to include special characters in strings.
# Common escape sequences:
# \n: Newline
# \t: Tab
# \\: Backslash
# \": Double quote
# \': Single quote


print("hellow world")

print("hellow world\nhow are you")

print("my name is \\ ganesh")

print("how are\" you")

print("how are\' you")

print("massage \t me")